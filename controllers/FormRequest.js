// Controllers for FormRequest
const FormRequest = require('../models/FormRequest');

module.exports = {
    getAllData: (req, res) => {
        FormRequest.find()
        .populate({ path:'idUser'})
        .then(result => {
            res.status(200).send({
                message: 'Get all data FormRequest',
                result
            })
        })
        .catch(error => {
            res.status(500).send({
                message: 'Internal server error',
                error
            })
        })
    },
    createData: (req, res) => {
        FormRequest.create(
            req.body,
        )
        .then(result => {
            res.status(200).send({
                message: 'success',
            })
        })
        .catch(error => {
            res.status(400).send({
                message: 'error',
                error
            })
        })
    },
    detailData: (req, res) => {
        const {id} = req.params;
        FormRequest.findOne({
            '_id': id
        })
        .populate({ path:'idUser'})
        .then(result => {
            res.status(200).send({
                message: 'Get all detail data FormRequest',
                result
            })
        })
        .catch(error => {
            res.status(400).send({
                message: 'Error',
                error
            })
        })
    },
    updateData: (req,res) => {
        const {id} = req.params;
        FormRequest.findOneAndUpdate({ 
            '_id' : id
        },req.body)
        .then(result => {
            res.status(200).send({
                message: 'success',
                result
            })
        })
        .catch(error => {
            res.status(400).send({
                message: 'error',
                error
            })
        })
    },
    deleteData : (req, res) => {
        const {id} = req.params;
        FormRequest.deleteOne({
            '_id' : id
        })
        .then(result => {
            res.status(200).send({
                message: 'success',
                result
            })
        })
        .catch(error => {
            res.status(400).send({
                message: 'error',
                error
            })
        })
    }
}